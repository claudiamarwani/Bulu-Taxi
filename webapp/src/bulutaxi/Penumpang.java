package bulutaxi;

public class Penumpang {

    String namaPenumpang;
    String idPenumpang;
    String lokasiAsal;
    String lokasiTujuan;
    String jamMulai;
    String jamSelesai;

    public String getNamaPenumpang() {
        return namaPenumpang;
    }
    
    public void setNamaPenumpang(String namaPenumpang) {
        this.namaPenumpang = namaPenumpang;
    }

    public String getIdPenumpang() {
        return idPenumpang;
    }

    public void setIdPenumpang(String idPenumpang) {
        this.idPenumpang = idPenumpang;
    }
    
    public String getLokasiAsal() {
        return lokasiAsal;
    }

    public void setLokasiAsal(String lokasiAsal) {
        this.lokasiAsal = lokasiAsal;
    }

    public String getLokasiTujuan() {
        return lokasiTujuan;
    }

    public void setLokasiTujuan(String lokasiTujuan) {
        this.lokasiTujuan = lokasiTujuan;
    }

    public String getJamMulai() {
        return jamMulai;
    }

    public void setJamMulai(String jamMulai) {
        this.jamMulai = jamMulai;
    }

    public String getJamSelesai() {
        return jamSelesai;
    }

    public void setJamSelesai(String jamSelesai) {
        this.jamSelesai = jamSelesai;
    }

}
